print('Você ditigou é alfabético?',(num1.isalpha()))
print('Você digitou tem númerico?',(num1.isnumeric()))
print('Você digitou tem espaços',(num1.isspace()))
print('Você digitou tem decimal?',(num1.isdecimal()))
print('Você digitou é alfanumérico?', (num1.isalnum()))
print('Você digitou é maiúsculas', (num1.isupper()))
print('Você digitou é minúsculas?', (num1.islower()))
print('Você digitou é capitalizada', (num1.istitle()))





